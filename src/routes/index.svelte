<h2 class="mt-8 text-lg text-red-800 mb-3">Ph.D Topic:</h2>
<div>
 <span class="title text-gray-700 tracking-wide">Denial-of-Sleep Attacks on IoT networks</span>
 <div class="para-1">The Internet of Things (IoT) has continued gaining in popularity and importance in everyday life in recent years. However, due to the number of sensitive and private data produced
	by IoT systems, IoT networks becomes the new privileged targets for cyberattackers. In addition, this devices are constraints in ressources and energy. Therefore, due to their characteristics, security systems developed for IoT networks are an open question in research. 
	At the same time, Machine Learning (ML) has gained a phenomenal success in various fields like telecommunications, transport or cybersecurity. Nonetheless, the application of ML can cause significant damage when put in the hands of an attacker. 
	The increasing use of machine learning in creating attacks is significantly changing the threat landscape in two notable ways. The first will be the expansion of existing attacks. Indeed, by integrating ML algorithms in current cyberattacks, these will become more resistant, more reactive, and less recognizable by the existing
	detection methods. The second will represent the creation of new threats which, until then, were not achievable due to their massive demand for data or their excessive manual processing time. </div>
 <div class="para-2 mt-4">
	To better understand the vulnerabilities that exist on IoT networks, my first objective is to take the place of an attacker. We focus on the creation of smart jamming attack. 
	Indeed, this simple attack can have serious consequences ranging from the loss of data to the decommissioning of an IOT device. Few solutions exist to counter smart jamming attacks. 
	Therefore, my first objective is to create smart jammming attacks in order to identify the flaws in the defense systems and then improve them.
