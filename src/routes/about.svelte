<h2 class="mt-8 text-lg text-red-800 mb-3">Ph.D Topic:</h2>
<div>
 <span class="title text-gray-700 tracking-wide">Denial-of-Sleep on IoT networks</span>
 <div class="para-1">The subject of my thesis is on attacks that can lead to denial-of-sleep on IoT networks. Nowadays IoT devices still have some constraints such as being limited in energy. 
	Attacks leading to Denial-of-sleep are based on this vulnerability. Their goal consists of forcing the victim node to waste its energy in order to put them out of service. At a time when IoT devices are more and more present in our lives and in critical infrastructures, it becomes essential to study this type of attack.
 </div>
 <div class="para-2">
	To better understand the vulnerabilities that exist on IoT networks my first objective is to put myself in the attacker's shoes in order to create attacks that can lead to a denial of sleep. We have focused for the moment on jamming attacks. We are also studying the possibility of creating intelligent attacks using machine learning in order to make these latter more efficient and more targeted. In the second time, my next objective will be to create solutions against this type of attack. 
	The final goal of this thesis is to make the communications protocols more robust as well as the detection systems against sleep denial attacks.
